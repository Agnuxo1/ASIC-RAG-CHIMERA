class EntropySource(threading.Thread):
    def __init__(self):
        super().__init__(daemon=True)
        self.share_buffer = deque(maxlen=1000)
        self.running = True
        self.sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        self.sock.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
        self.sock.bind(("0.0.0.0", 3333))
        self.sock.listen(1)
        self.connection_event = threading.Event()
        
    def run(self):
        print("[ASIC] Listening for miner connection...")
        conn, addr = self.sock.accept()
        print(f"[ASIC] Miner connected: {addr}")
        self.connection_event.set()
        
        # Basic stratum handshake
        buffer = ""
        while self.running:
            try:
                data = conn.recv(4096).decode('utf-8', errors='ignore')
                if not data: break
                buffer += data
                while '\n' in buffer:
                    line, buffer = buffer.split('\n', 1)
                    self._process_line(conn, line)
            except Exception as e:
                print(f"Error: {e}")
                break
                
    def _process_line(self, conn, line):
        try:
            msg = json.loads(line)
            method = msg.get('method')
            mid = msg.get('id')
            
            if method == 'mining.subscribe':
                resp = {"id": mid, "result": [[["mining.set_difficulty","1"],["mining.notify","1"]], "08000002", 4], "error": None}
                conn.sendall((json.dumps(resp)+'\n').encode())
                # High difficulty to slow down shares for analysis, or low for flood?
                # We want frequent shares to capture micro-timing.
                conn.sendall((json.dumps({"id":None,"method":"mining.set_difficulty","params":[0.5]})+'\n').encode())
                self._send_job(conn)
                
            elif method == 'mining.authorize':
                conn.sendall((json.dumps({"id": mid, "result": True, "error": None})+'\n').encode())
                
            elif method == 'mining.submit':
                # === HERE IS THE PHYSICS ===
                # The arrival time of the share is the entropy source.
                # We capture it with nanosecond precision (if OS allows)
                arrival_time = time.perf_counter_ns() 
                self.share_buffer.append(arrival_time)
                
                conn.sendall((json.dumps({"id": mid, "result": True, "error": None})+'\n').encode())
                
        except: pass
        
    def _send_job(self, conn):
        # Static job just to keep ASIC hashing
        job = {"id":None,"method":"mining.notify","params":["0", "0"*64, "0100000001"+"0"*56, "ffffffff", [], "20000000", "1f00ffff", hex(int(time.time()))[2:], True]}
        conn.sendall((json.dumps(job)+'\n').encode())

    def get_entropy_batch(self, size=10) -> List[float]:
        """
        Returns normalized entropy values from inter-arrival times.
        """
        while len(self.share_buffer) < size + 1:
            time.sleep(0.1)
            
        times = list(self.share_buffer)[-size-1:]
        # Calculate deltas (inter-arrival times)
        deltas = [t2 - t1 for t1, t2 in zip(times, times[1:])]
        
        # Normalize to 0.0 - 100.0 range for current injection
        # Use log scale to compress the long tail of Poisson distribution
        normalized = [math.log(d + 1) % 100.0 for d in deltas]
        return normalized

