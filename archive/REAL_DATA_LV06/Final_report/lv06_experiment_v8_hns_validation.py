class HNSNumber:
    """
    Implementación en Python puro del Sistema Numérico Jerárquico de Veselov.
    Representa números como polinomios de coeficientes enteros sobre una base B.
    N = sign * sum(d_i * B^i)
    Evita la pérdida de precisión de float32.
    """
    BASE = 1_000_000_000  # Base 10^9 para alinear con nanosegundos
    LEVELS = 4            # Profundidad de la jerarquía

    def __init__(self, value: Union[float, int, List[int]]):
        self.coeffs = [0] * self.LEVELS
        self._sign = 1  # Store sign separately to avoid zero-coefficient bug
        
        if isinstance(value, list):
            self.coeffs = value[:self.LEVELS]
        elif isinstance(value, int):
            self._from_int(value)
        elif isinstance(value, float):
            self._from_float(value)
            
    def _from_int(self, val: int):
        """Handle signed integers properly."""
        self._sign = 1 if val >= 0 else -1
        val = abs(val)
        for i in range(self.LEVELS):
            self.coeffs[i] = val % self.BASE
            val //= self.BASE

    def _from_float(self, val: float):
        # Convert float to fixed-point representation within HNS
        # Scale float to integer space
        scaled = int(val * (self.BASE ** 2)) 
        self._from_int(scaled)

    def to_float(self) -> float:
        """Regresa a float solo para visualización/logging"""
        scale = self.BASE ** 2
        val = 0
        for i, c in enumerate(self.coeffs):
            val += c * (self.BASE ** i)
        return (self._sign * val) / scale

    def add(self, other: 'HNSNumber') -> 'HNSNumber':
        res = [0] * self.LEVELS
        carry = 0
        for i in range(self.LEVELS):
            s = self.coeffs[i] + other.coeffs[i] + carry
            res[i] = s % self.BASE
            carry = s // self.BASE
        return HNSNumber(res)

    def sub(self, other: 'HNSNumber') -> 'HNSNumber':
        res = [0] * self.LEVELS
        borrow = 0
        for i in range(self.LEVELS):
            s = self.coeffs[i] - other.coeffs[i] - borrow
            if s < 0:
                s += self.BASE
                borrow = 1
            else:
                borrow = 0
            res[i] = s
        return HNSNumber(res)

    def mul_scalar(self, factor: float) -> 'HNSNumber':
        # Aproximación simple para PoC: multiplicar coeficientes y renormalizar
        # En la implementación completa GPU, esto usa el algoritmo de Karatsuba
        # Aquí usamos conversión intermedia de alta precisión de Python (int ilimitado)
        val_int = 0
        for i, c in enumerate(self.coeffs):
            val_int += c * (self.BASE ** i)
        val_int *= self._sign  # Apply stored sign
        
        res_int = int(val_int * factor)
        return HNSNumber(res_int) # Re-encode

    def __repr__(self):
        return f"HNS<{self.coeffs}>"

